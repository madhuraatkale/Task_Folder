package edu;

public class ArithmeticOp {

	public static void main(String[] args) {
		int num1 =5;
		int num2 =4;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2); //
        
		int a=4;
		int b=2;
		System.out.println(a % b);
	}

}
